export const LOCALES = ["en", "fr", "ar"] as const;
export type Locale = (typeof LOCALES)[number];

export const DEFAULT_LOCALE: Locale = "en";

export const LOCALE_LABELS: Record<Locale, string> = {
  en: "EN",
  fr: "FR",
  ar: "AR",
};

export const LOCALE_NAMES: Record<Locale, string> = {
  en: "English",
  fr: "Français",
  ar: "العربية",
};

export function isLocale(value: string | undefined | null): value is Locale {
  return !!value && (LOCALES as readonly string[]).includes(value);
}

export function localeFromPathname(pathname: string | null | undefined): Locale {
  const first = (pathname || "/").split("/").filter(Boolean)[0];
  return isLocale(first) ? first : DEFAULT_LOCALE;
}

export function stripLocale(pathname: string): string {
  const parts = pathname.split("/").filter(Boolean);
  if (isLocale(parts[0])) parts.shift();
  return `/${parts.join("/")}`.replace(/\/$/, "") || "/";
}

export function withLocale(href: string, locale: Locale): string {
  if (/^(mailto:|tel:|https?:|#)/.test(href)) return href;
  const [path, hashOrQuery = ""] = href.split(/(?=[?#])/);
  const clean = stripLocale(path || "/");
  const localized = locale === DEFAULT_LOCALE ? clean : `/${locale}${clean === "/" ? "" : clean}`;
  return `${localized}${hashOrQuery}`;
}

export function switchLocaleHref(pathname: string, search: string, locale: Locale): string {
  const clean = stripLocale(pathname || "/");
  return `${locale === DEFAULT_LOCALE ? clean : `/${locale}${clean === "/" ? "" : clean}`}${search || ""}`;
}

export function dirForLocale(locale: Locale): "ltr" | "rtl" {
  return locale === "ar" ? "rtl" : "ltr";
}

